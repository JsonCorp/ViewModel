package com.json.viewmodel;

public class Item {
    private String mName;
    private String mNumber;

    public void setName(String name) {
        mName = name;
    }
    public void setNumber(String number) {
        mNumber = number;
    }

    public String getName() {
        return mName;
    }
    public String getNumber() {
        return mNumber;
    }
}
